const rules = [
  [/#{6}\s?([^\n]+)\n/g, '<h6>$1</h6>'],
  [/#{5}\s?([^\n]+)\n/g, '<h5>$1</h5>'],
  [/#{4}\s?([^\n]+)\n/g, '<h4>$1</h4>'],
  [/#{3}\s?([^\n]+)\n/g, '<h3>$1</h3>'],
  [/#{2}\s?([^\n]+)\n/g, '<h2>$1</h2>'],
  [/#{1}\s?([^\n]+)\n/g, '<h1>$1</h1>'],
  [/\*{2}(.+)\*{2}/g, '<b>$1</b>'],
  [/([^\n]+)\n\s+=+/g, '<h1>$1</h1><hr/>'],
  [/([^\n]+)\n\s+-+/g, '<h2>$1</h2><hr/>'],
  [/\*(.+)\*/g, '<i>$1</i>'],
  [/__([^_]+)__/g, '<b>$1</b>'],
  [/_([^_]+)_/g, '<i>$1</i>'],
  [/((\n\d\..+)+)/g, '<ol>$1</ol>'],
  [/((\n\*.+)+)/g, '<ul>$1</ul>'],
  [/\n\d\.([^\n]+)/g, '<li>$1</li>'],
  [/\n\*([^\n]+)/g, '<li>$1</li>'],
  [/!\[([^\\[]+)\]\(([^\\)]+)\)/g, '<img src="$2" alt="$1" />'],
  [/\[([^\\[]+)\]\(([^\\)]+)\)/g, '<a href="$2">$1</a>'],  
];
export default {
  bind(el) {
    let html = el.textContent;
    rules.forEach(([rule, template]) => {
      html = html.replace(rule, template);
    })
    el.innerHTML = html;
  }
}